let aspect = window.innerWidth / window.innerHeight;

let userCamera = 0;

let scene = new THREE.Scene();

let renderer = new THREE.WebGLRenderer({
    antialias: true,
    devicePixelRatio: window.devicePixelRatio
});

document.body.appendChild(renderer.domElement);

let maze = Maze(matrix, renderer);

scene.add(maze);

updateCamera();
animate();

function updateCamera() {
    aspect = window.innerWidth / window.innerHeight;

    maze.updateCamera();
    maze.character.updateCamera();

    renderer.setSize(window.innerWidth, window.innerHeight);
}

function render() {
    if (userCamera) {
        maze.character.orbit.enabled = true;
        renderer.render(scene, maze.character.camera);
    }
    else {
        maze.character.orbit.enabled = false;
        renderer.render(scene, maze.camera);
    }
}

window.addEventListener('resize', function() {
    updateCamera();
    render();
});

document.body.addEventListener('keydown', (event) => {
    maze.keyDownHolder(event);
});

document.body.addEventListener('keydown', (event) => {
    switch (event.key) {
        case '1':
        userCamera = 0;
        break;
        case '2':
        userCamera = 1;
        break;
    }
});

function animate() {
    window.requestAnimationFrame(animate);
    maze.animation();
    render();
}